package variables;

public class DemoVariable {
    public static void main(String[] args) {
        String CityName;
        CityName="Kanpur";
        System.out.println("City Name:"+CityName);
        String StudentName;
        StudentName="Ajeet Chauhan";
        System.out.println("Student Name:"+StudentName);
        String Class;
        Class="BCA";
        System.out.println("Class:"+Class);
        Class="MCA";
        System.out.println("Variable Value Change:- "+Class);
        int Age;
        Age=23;
        System.out.println("Age:"+Age);
        int Subject;
        Subject=4;
        System.out.println("Subject:"+Subject);
        int Semester;
        Semester=3;
        System.out.println("Semester:"+Semester);
        int ClassHour;
        ClassHour=1;
        System.out.println("Class Hour:"+ClassHour);
        double TheoryMarks;
        TheoryMarks=78.6;
        System.out.println("Theory Marks:"+TheoryMarks);
        double PracticalMarks;
        PracticalMarks=79.2;
        System.out.println("Practical Marks:"+PracticalMarks);
        double Java;
        Java=76.3;
        System.out.println("Java:"+Java);


    }
}
